# ── 풀 관리 ──────────────────────────────────────────────────────────────────

# 각 풀의 셔플된 순열 (세션 내 중복 최소화)
_title_pool: list[str] = []
_neutral_pool: list[str] = []

# 카테고리별 순열 풀 (세션 내 중복 최소화)
_category_pools: dict[str, list[str]] = {}

# 중립:제목 비율 = 7:3
_NEUTRAL_WEIGHT = 0.7


def _refill(pool: list[str], source: list[str]) -> None:
    pool.extend(random.sample(source, len(source)))


def pick_phrase(title: str, category: str | None = None) -> str:
    """
    게시물 제목을 받아 댓글 문구 반환.

    category가 주어지면 카테고리별 전용 문구에서 선택.
    category가 없으면 70% 중립 문구, 30% 제목 포함 문구 선택.
    각 풀은 순열 방식으로 소진 후 재셔플 — 단기 반복 최소화.

    Args:
        title: 게시물 제목 (제목 포함 문구에 삽입)
        category: 카테고리 키 (맛집/여행/일상/IT/뷰티/육아/재테크/리뷰). 없으면 기본 동작.

    Returns:
        선택된 댓글 문구 문자열
    """
    global _title_pool, _neutral_pool, _category_pools

    # 카테고리별 전용 문구 사용
    if category and category in _CATEGORY_PHRASES:
        pool = _category_pools.get(category)
        if pool is None:
            _category_pools[category] = []
            pool = _category_pools[category]
        if not pool:
            _refill(pool, _CATEGORY_PHRASES[category])
        return pool.pop()

    # 기존 동작: 중립 / 제목 포함
    use_neutral = random.random() < _NEUTRAL_WEIGHT

    if use_neutral:
        if not _neutral_pool:
            _refill(_neutral_pool, _NEUTRAL_PHRASES)
        return _neutral_pool.pop()
    else:
        if not _title_pool:
            _refill(_title_pool, _TITLE_PHRASES)
        template = _title_pool.pop()
        short_title = title[:TITLE_MAX_LEN] + ("..." if len(title) > TITLE_MAX_LEN else "")
        safe_title = short_title.replace("{", "").replace("}", "")
        try:
            return template.format(title=safe_title)
        except (KeyError, ValueError, IndexError):
            return template.replace("{title}", safe_title)
